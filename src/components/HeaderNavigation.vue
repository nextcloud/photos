<template>
	<div :class="{ 'photos-navigation--root': isRoot }" class="photos-navigation" role="toolbar">
		<!-- Back navigation button -->
		<NcButton
			v-if="!isRoot"
			class="photos-navigation__back"
			:aria-label="t('photos', 'Go back')"
			type="tertiary"
			@click="folderUp">
			<template #icon>
				<ArrowLeft :size="20" />
			</template>
		</NcButton>

		<!-- Main Navigation title -->
		<div class="photos-navigation__title">
			<h1 class="photos-navigation__title__main" @click="refresh">
				{{ name }}
			</h1>
			<div class="photos-navigation__title__sub" />
			<slot name="subtitle" />
		</div>

		<!-- Main slot -->
		<div class="photos-navigation__content">
			<div v-if="$slots.default" class="photos-navigation__content__left">
				<slot />
			</div>

			<NcLoadingIcon v-show="loading" class="photos-navigation__loader" />

			<div class="photos-navigation__content__right">
				<slot name="right" />
			</div>
		</div>
	</div>
</template>

<script lang='ts'>
import { t } from '@nextcloud/l10n'
import { NcButton, NcLoadingIcon } from '@nextcloud/vue'
import ArrowLeft from 'vue-material-design-icons/ArrowLeft.vue'

export default {
	name: 'HeaderNavigation',

	components: {
		ArrowLeft,
		NcButton,
		NcLoadingIcon,
	},

	inheritAttrs: false,

	props: {
		loading: {
			type: Boolean,
			default: false,
		},

		path: {
			type: String,
			default: '/',
		},

		title: {
			type: String,
			required: true,
		},

		rootTitle: {
			type: String,
			default: t('photos', 'Photos'),
		},

		// The route params
		params: {
			type: Object,
			default: null,
		},
	},

	computed: {
		isRoot() {
			const isRoot = this.path === '/'
			this.toggleNavigationButton(!isRoot)
			return isRoot
		},

		name() {
			if (this.isRoot) {
				return this.rootTitle
			}
			return this.title
		},
	},

	methods: {
		folderUp() {
			this.$router.push(this.$route.path.split('/').slice(0, -1).join('/'))
		},

		refresh() {
			this.$emit('refresh')
		},

		toggleNavigationButton(hide) {
			// Hide the navigation toggle if the back button is shown
			const navigationToggle = document.querySelector('button.app-navigation-toggle') as HTMLElement
			if (navigationToggle !== null && hide) {
				navigationToggle.style.display = 'none'
			}
		},

		t,
	},
}
</script>

